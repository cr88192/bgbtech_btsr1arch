int BGBCC_JX2_TryDisassembleOpcodeBuf(
	BGBCC_JX2_Context *ctx, char **rct,
	int pc, int opw1, int opw2)
{
	BGBCC_JX2_OpcodeArg arg0;
	BGBCC_JX2_OpcodeArg arg1;
	BGBCC_JX2_OpcodeArg arg2;
	char *ct, *ct0, *s0, *snm;
	int nmid, fmid, wex2, il;
	int i, j;
	
	BGBCC_JX2_DisassembleBuildOpIdx();
	
	il=BGBCC_JX2_TryDisassembleOpcodeI1(ctx, opw1|(opw2<<16),
		&nmid, &fmid, &wex2, &arg0, &arg1, &arg2);

	if(il<0)
	{
		nmid=0;
		il=1;
		if((opw1&0xE000)==0xE000)
			il=2;
		if((opw1&0xF000)==0x7000)
			il=2;
		if((opw1&0xF000)==0x9000)
			il=2;

		if(ctx->is_fixed32&1)
			il=2;
	}

	snm=BGBCC_JX2DA_NmidToName(ctx, nmid, wex2);

	ct=*rct;
	ct0=ct;

	if(il>1)
		sprintf(ct, "  %04X_%04X  %-10s ", opw1, opw2, snm);
	else
		sprintf(ct, "  %04X       %-10s ", opw1, snm);
	ct+=strlen(ct);
	BGBCC_JX2_TryDisassembleOpcode_PrintArgStr(ctx, &ct, &arg0, nmid);
	if(arg1.ty!=BGBCC_SH_OPVTY_NONE)
		{ strcpy(ct, ", "); ct+=strlen(ct); }
	BGBCC_JX2_TryDisassembleOpcode_PrintArgStr(ctx, &ct, &arg1, nmid);
	if(arg2.ty!=BGBCC_SH_OPVTY_NONE)
		{ strcpy(ct, ", "); ct+=strlen(ct); }
	BGBCC_JX2_TryDisassembleOpcode_PrintArgStr(ctx, &ct, &arg2, nmid);

	s0=BGBCC_JX2DA_GetIstrSuffix(ctx, wex2);
	
	if(*s0!='\n')
	{
		j=ct-ct0;
		if(j<35)
		{
			memset(ct, ' ', 64);
			ct+=35-j;
			
			while(*s0 && (*s0<=' '))
				s0++;
		}
	}
	
	strcpy(ct, s0);
	ct+=strlen(ct);

	*rct=ct;
	return(il);
}

