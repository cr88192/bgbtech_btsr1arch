int BTM_DrawGlyphFBuf(u16 *fbuf, int ystr, int cx, int cy,
	u64 pix, u16 c1, u16 c0)
{
	u16 *ct;
	int y, p1;
	
	ct=fbuf+(cy*ystr)+cx;
	for(y=0; y<8; y++)
	{
		p1=(pix>>((7-y)*8))&255;

		if(p1&0x80)		{ ct[0]=c1; }
		else			{ ct[0]=c0; }
		if(p1&0x40)		{ ct[1]=c1; }
		else			{ ct[1]=c0; }
		if(p1&0x20)		{ ct[2]=c1; }
		else			{ ct[2]=c0; }
		if(p1&0x10)		{ ct[3]=c1; }
		else			{ ct[3]=c0; }

		if(p1&0x08)		{ ct[4]=c1; }
		else			{ ct[4]=c0; }
		if(p1&0x04)		{ ct[5]=c1; }
		else			{ ct[5]=c0; }
		if(p1&0x02)		{ ct[6]=c1; }
		else			{ ct[6]=c0; }
		if(p1&0x01)		{ ct[7]=c1; }
		else			{ ct[7]=c0; }

		ct+=ystr;
	}
	return(0);
}

int BTM_DrawCharFBuf(u16 *fbuf, int ystr, int x, int y,
	byte ch, u16 c1, u16 c0)
{
	BTM_DrawGlyphFBuf(fbuf, ystr, x, y, btesh2_gfxcon_glyphs[ch], c1, c0);
	return(0);
}
